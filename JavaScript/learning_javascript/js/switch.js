var number = prompt("Digite um número:")

switch(number){
    case "1":
        //Se o parametro(number) == 1
        document.write("Sei lá")
        break
    case "2":
        //Se o parametro(number) == 2
        document.write("Sei lá só que 2")
        break
    default:
        // Se não for nenhum dos parâmetros(optativo)
        document.write("Nada deu certo")
        break
} 